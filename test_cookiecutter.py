"""Tests for the cookiecutter-rapyd-django template.

Tests bake the template into a temp directory and assert:
- The bake succeeds and the post-gen hook runs cleanly.
- All expected files are present; legacy files are absent.
- The generated project passes ruff and pytest.
- python_version and author fields render into pyproject.toml.
- The pre-gen hook rejects invalid slugs, emails, and unsafe chars.
- The post-gen hook fails gracefully when uv is missing from PATH.

Note: cookies.bake() runs the post-gen hook, which calls `git init` and
`uv sync`. Tests that call cookies.bake() require git, uv, and network
access. uv caches packages locally so only the first run downloads.
"""

from __future__ import annotations

import os
import subprocess
import sys
from pathlib import Path

import pytest

# Strip parent-process venv/Python pins so uv commands in the generated project
# use the project's own .venv rather than the test runner's interpreter.
_ISOLATED_ENV = {
    k: v
    for k, v in os.environ.items()
    if k not in ("VIRTUAL_ENV", "VIRTUAL_ENV_PROMPT", "UV_PYTHON")
}

# Default slug derived from the default project_name "Django Boilerplate".
DEFAULT_SLUG = "django_boilerplate"

# Files that must exist in the rendered project tree (static, from template).
EXPECTED_FILES = [
    ".editorconfig",
    ".env.example",
    ".gitignore",
    ".pre-commit-config.yaml",
    "AGENTS.md",
    "gunicorn_start",
    "LICENSE",
    "Makefile",
    "pyproject.toml",
    "README.md",
    "supervisor.conf",
    f"{DEFAULT_SLUG}/manage.py",
    f"{DEFAULT_SLUG}/config/__init__.py",
    f"{DEFAULT_SLUG}/config/settings.py",
    f"{DEFAULT_SLUG}/config/urls.py",
    f"{DEFAULT_SLUG}/config/wsgi.py",
    f"{DEFAULT_SLUG}/config/asgi.py",
    "tests/__init__.py",
    "tests/test_smoke.py",
    ".github/workflows/test.yml",
]

# Files generated by the post-gen hook (git init + uv sync + pre-commit).
EXPECTED_GENERATED_FILES = [
    ".env",
    "uv.lock",
    "requirements.txt",
    "requirements-dev.txt",
]

# Files that must NOT exist — removed in the packaging modernisation.
OBSOLETE_FILES = [
    "Pipfile",
    "Pipfile.lock",
    ".flake8",
    ".isort.cfg",
    "setup_env.sh",
    # Note: readme.md → README.md rename is verified by README.md in EXPECTED_FILES.
    # macOS APFS is case-insensitive so asserting readme.md is absent would always
    # fail when README.md exists.
]


def _run(cmd: list[str], cwd: Path) -> subprocess.CompletedProcess:
    return subprocess.run(
        cmd, cwd=cwd, env=_ISOLATED_ENV, capture_output=True, text=True
    )


