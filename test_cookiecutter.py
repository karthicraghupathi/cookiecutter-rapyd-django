def test_non_ascii_author_accepted(cookies):
    """Non-ASCII characters in author_name are valid — only TOML-unsafe chars blocked.

    |tojson renders non-ASCII as unicode escapes (ASCII-safe JSON), so the raw
    TOML contains e.g. "Ren\\u00e9e" rather than the literal é glyph. Both are
    valid TOML; we check the ASCII suffix to avoid encoding ambiguity.
    """
    result = cookies.bake(extra_context={"author_name": "Renée Dupont"})

    assert result.exit_code == 0, result.exception
    pyproject = (result.project_path / "pyproject.toml").read_text(encoding="utf-8")
    # "Dupont" is unique to this author; é is escaped but the ASCII suffix is not.
    assert "Dupont" in pyproject
