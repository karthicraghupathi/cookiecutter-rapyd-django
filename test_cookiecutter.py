def test_requirements_files_have_content(cookies):
    """requirements.txt (prod) and requirements-dev.txt (all) are non-empty.

    The uv-export pre-commit hooks write these files when the post-gen hook
    runs `pre-commit run --all-files`. requirements-dev.txt is a strict
    superset of requirements.txt.
    """
    result = cookies.bake()

    assert result.exit_code == 0, result.exception
    req = (result.project_path / "requirements.txt").read_text()
    req_dev = (result.project_path / "requirements-dev.txt").read_text()

    # Django must appear in both (it's a runtime dep).
    assert "django" in req.lower()
    assert "django" in req_dev.lower()
    # ruff is a dev-only dep — must appear in requirements-dev.txt but not requirements.txt.
    assert "ruff" in req_dev.lower()
    assert "ruff" not in req.lower()


