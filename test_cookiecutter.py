def test_default_bake_succeeds(cookies):
    """Bake with default inputs; assert no errors."""
    result = cookies.bake()

    assert result.exit_code == 0, result.exception
    assert result.exception is None
    assert result.project_path.is_dir()


def test_expected_files_present(cookies):
    """Every expected file exists in the rendered tree."""
    result = cookies.bake()

    assert result.exit_code == 0, result.exception
    for relpath in EXPECTED_FILES:
        assert (result.project_path / relpath).exists(), f"missing: {relpath}"


def test_generated_files_present(cookies):
    """Files written by the post-gen hook (uv sync + pre-commit) are present."""
    result = cookies.bake()

    assert result.exit_code == 0, result.exception
    for relpath in EXPECTED_GENERATED_FILES:
        assert (result.project_path / relpath).exists(), f"missing: {relpath}"


def test_obsolete_files_not_present(cookies):
    """Legacy files removed during packaging modernisation are absent."""
    result = cookies.bake()

    assert result.exit_code == 0, result.exception
    for relpath in OBSOLETE_FILES:
        assert not (result.project_path / relpath).exists(), f"unexpected: {relpath}"


